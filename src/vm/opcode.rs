use std::convert::TryFrom;

#[repr(u8)]
#[derive(Debug, Copy, Clone, PartialEq, Eq)]
pub enum Opcode {
    Push64 = 0x00,
    Push256 = 0x01,
    PushAddr = 0x02,
    PushLocal = 0x03,
    StoreLocal = 0x04,
    Pop = 0x05,
    Dup = 0x06,
    Swap = 0x07,
    Add64 = 0x10,
    Sub64 = 0x11,
    Mul64 = 0x12,
    Div64 = 0x13,
    Mod64 = 0x14,
    Shl64 = 0x15,
    Shr64 = 0x16,
    Eq64 = 0x17,
    Lt64 = 0x18,
    Gt64 = 0x19,
    Add256 = 0x20,
    Sub256 = 0x21,
    Mul256 = 0x22,
    Div256 = 0x23,
    Mod256 = 0x24,
    Shl256 = 0x25,
    Shr256 = 0x26,
    Eq256 = 0x27,
    Lt256 = 0x28,
    Gt256 = 0x29,
    And = 0x30,
    Or = 0x31,
    Xor = 0x32,
    Not = 0x33,
    IsZero = 0x34,
    Cast64To256 = 0x40,
    Cast256To64 = 0x41,
    CastAddrTo256 = 0x42,
    Cast256ToAddr = 0x43,
    Jump = 0x50,
    Jumpi = 0x51,
    Return = 0x52,
    Revert = 0x53,
    Stop = 0x54,
    Mload64 = 0x60,
    Mstore64 = 0x61,
    Mload256 = 0x62,
    Mstore256 = 0x63,
    Mcopy = 0x64,
    Mstore8 = 0x65,
    Mload8 = 0x66,
    New = 0x70,
    GetField = 0x71,
    SetField = 0x72,
    GetState = 0x73,
    SetState = 0x74,
    NewMap = 0x75,
    MapGet = 0x76,
    MapSet = 0x77,
    NewArray = 0x78,
    ArrayGet = 0x79,
    ArraySet = 0x7A,
    ArrayLen = 0x7B,
    Caller = 0x80,
    CallValue = 0x81,
    Address = 0x82,
    Balance = 0x83,
    BlockNum = 0x84,
    Timestamp = 0x85,
    Sha3 = 0x86,
    ChainId = 0x87,
    Origin = 0x88,
    GasLeft = 0x89,
    GasPrice = 0x8A,
    ReturnDataSize = 0x8B,
    Invoke = 0x90,
    InvokeStatic = 0x91,
    InvokeDelegate = 0x92,
    InvokeInterface = 0x93,
    InvokeItfStatic = 0x94,
    Emit = 0x95,
    CallRaw = 0xA0,
    CallDataLoad = 0xA1,
    ReturnDataCopy = 0xA2,
    Invalid = 0xFF,
}

impl TryFrom<u8> for Opcode {
    type Error = ();

    fn try_from(value: u8) -> Result<Self, Self::Error> {
        match value {
            0x00 => Ok(Opcode::Push64),
            0x01 => Ok(Opcode::Push256),
            0x02 => Ok(Opcode::PushAddr),
            0x03 => Ok(Opcode::PushLocal),
            0x04 => Ok(Opcode::StoreLocal),
            0x05 => Ok(Opcode::Pop),
            0x06 => Ok(Opcode::Dup),
            0x07 => Ok(Opcode::Swap),
            0x10 => Ok(Opcode::Add64),
            0x11 => Ok(Opcode::Sub64),
            0x12 => Ok(Opcode::Mul64),
            0x13 => Ok(Opcode::Div64),
            0x14 => Ok(Opcode::Mod64),
            0x15 => Ok(Opcode::Shl64),
            0x16 => Ok(Opcode::Shr64),
            0x17 => Ok(Opcode::Eq64),
            0x18 => Ok(Opcode::Lt64),
            0x19 => Ok(Opcode::Gt64),
            0x20 => Ok(Opcode::Add256),
            0x21 => Ok(Opcode::Sub256),
            0x22 => Ok(Opcode::Mul256),
            0x23 => Ok(Opcode::Div256),
            0x24 => Ok(Opcode::Mod256),
            0x25 => Ok(Opcode::Shl256),
            0x26 => Ok(Opcode::Shr256),
            0x27 => Ok(Opcode::Eq256),
            0x28 => Ok(Opcode::Lt256),
            0x29 => Ok(Opcode::Gt256),
            0x30 => Ok(Opcode::And),
            0x31 => Ok(Opcode::Or),
            0x32 => Ok(Opcode::Xor),
            0x33 => Ok(Opcode::Not),
            0x34 => Ok(Opcode::IsZero),
            0x40 => Ok(Opcode::Cast64To256),
            0x41 => Ok(Opcode::Cast256To64),
            0x42 => Ok(Opcode::CastAddrTo256),
            0x43 => Ok(Opcode::Cast256ToAddr),
            0x50 => Ok(Opcode::Jump),
            0x51 => Ok(Opcode::Jumpi),
            0x52 => Ok(Opcode::Return),
            0x53 => Ok(Opcode::Revert),
            0x54 => Ok(Opcode::Stop),
            0x60 => Ok(Opcode::Mload64),
            0x61 => Ok(Opcode::Mstore64),
            0x62 => Ok(Opcode::Mload256),
            0x63 => Ok(Opcode::Mstore256),
            0x64 => Ok(Opcode::Mcopy),
            0x65 => Ok(Opcode::Mstore8),
            0x66 => Ok(Opcode::Mload8),
            0x70 => Ok(Opcode::New),
            0x71 => Ok(Opcode::GetField),
            0x72 => Ok(Opcode::SetField),
            0x73 => Ok(Opcode::GetState),
            0x74 => Ok(Opcode::SetState),
            0x75 => Ok(Opcode::NewMap),
            0x76 => Ok(Opcode::MapGet),
            0x77 => Ok(Opcode::MapSet),
            0x78 => Ok(Opcode::NewArray),
            0x79 => Ok(Opcode::ArrayGet),
            0x7A => Ok(Opcode::ArraySet),
            0x7B => Ok(Opcode::ArrayLen),
            0x80 => Ok(Opcode::Caller),
            0x81 => Ok(Opcode::CallValue),
            0x82 => Ok(Opcode::Address),
            0x83 => Ok(Opcode::Balance),
            0x84 => Ok(Opcode::BlockNum),
            0x85 => Ok(Opcode::Timestamp),
            0x86 => Ok(Opcode::Sha3),
            0x87 => Ok(Opcode::ChainId),
            0x88 => Ok(Opcode::Origin),
            0x89 => Ok(Opcode::GasLeft),
            0x8A => Ok(Opcode::GasPrice),
            0x8B => Ok(Opcode::ReturnDataSize),
            0x90 => Ok(Opcode::Invoke),
            0x91 => Ok(Opcode::InvokeStatic),
            0x92 => Ok(Opcode::InvokeDelegate),
            0x93 => Ok(Opcode::InvokeInterface),
            0x94 => Ok(Opcode::InvokeItfStatic),
            0x95 => Ok(Opcode::Emit),
            0xA0 => Ok(Opcode::CallRaw),
            0xA1 => Ok(Opcode::CallDataLoad),
            0xA2 => Ok(Opcode::ReturnDataCopy),
            0xFF => Ok(Opcode::Invalid),
            _ => Err(()),
        }
    }
}
